public class LinearSearchprogram {

	public static void main(String[] args) {
		
		
		int[] arr = {1,2,3,5,6,87,100};
		
		int target = 6;
		
		
		for(int i=0;i<arr.length;i++) {
			
			if(arr[i]==target) {
				
				
				System.out.println("Target Found at index:"+i);
			}
			
			
			
		}
		
		

	}

}
