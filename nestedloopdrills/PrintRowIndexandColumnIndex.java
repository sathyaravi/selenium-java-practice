package nestedloopdrills;

public class PrintRowIndexandColumnIndex {

	public static void main(String[] args) {
		
		for(int row=0;row<3;row++) {

			for(int col=0;col<3;col++) {
				
				System.out.println("Row Index:"+row+", Column Index:"+col+" ");
			}
			
			System.out.println();
		}

	}

}
